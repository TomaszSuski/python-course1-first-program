# podstawowe operatory matematyczne, gdzie a i b to liczby:
# a + b - dodawanie
# a - b - odejmowanie
# a * b - mnożenie
# a / b - dzielenie (wynik jest floatem)
# a // b - dzielenie całkowite (wynik jest intem, z zaokrąglaniem w dół)

# kolejność wykonywania działań jest taka sama jak w matematyce
# np. 9 + 8 * 5 - 6 = 9 + 40 - 6 = 43
# nawiasy mają pierwszeństwo

# potegowanie
# a ** b - a do potęgi b

# reszta z dzielenia
# a % b - reszta z dzielenia a przez b

# operatory przypisania
# a = b - przypisanie
# a, b, c = 1, 2, 3 - przypisanie wielu wartości do wielu zmiennych (a = 1, b = 2, c = 3)
# a += b - a = a + b
# a -= b - a = a - b
# a *= b - a = a * b
# a /= b - a = a / b
# a //= b - a = a // b
# a **= b - a = a ** b
# a %= b - a = a % b

# operatory porównania zwrają wartość True lub False (w pythonie z wielkiej litery!)
# a == b - a jest równe b
# a != b - a nie jest równe b
# a > b - a jest większe od b
# a < b - a jest mniejsze od b
# a >= b - a jest większe lub równe b
# a <= b - a jest mniejsze lub równe b

# operatory logiczne
# and - i
# or - lub
# not - nie (stawiany przed wyrażeniem logicznym, np, not True = False)
